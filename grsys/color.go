package grsys

import (
	"fmt"
	"image/color"
	"os"
)

var (
	NColors     int
	ForeGrColor int = 14
	BackGrColor int = 0

	palette color.Palette
)

func SetColor(c int) {
	ForeGrColor = c
}

func SetBackgroundColor(c int) {
	BackGrColor = c
}

func GetMaxColor() int {
	return 255
}

func SetRGBPalette(index, r, g, b int) {
	palette[index] = color.RGBA{
		R: uint8(r * 4),
		G: uint8(g * 4),
		B: uint8(b * 4),
		A: 255,
	}
}

func ShadedColors() {
	f, err := os.Open("palette.txt")
	if err == nil {
		for i := 0; i < NColors; i++ {
			var r, g, b int
			if _, err := fmt.Fscanln(f, &r, &g, &b); err != nil {
				Error(err)
			}
			SetRGBPalette(i, r, g, b)
		}
	} else {
		SetRGBPalette(0, 0, 0, 63) // blue background
		for i := 1; i < NColors; i++ {
			j := 4*i + 3
			SetRGBPalette(i, j, j, 0) // shades of yellow
		}
	}
}

